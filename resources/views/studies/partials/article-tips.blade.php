<h2>Tips: </h2>
	
<ul>
	<li>You can use (CTRL+S / CMD+S) to save your changes.</li>
	<li>Simply refresh page without pressing 'save' on form to cancel any changes you made.</li>
	<li>Thoughtfully choose your title as changing it in the future may produce undesired results.</li>
	<li>A colon (:) in the title tells us that your new study is the child of the preceding text. So, only use a colon (:) in that case.</li>
	<li>The comment is only displayed in the version history and is used to make sense of the version history of your study by telling us what kind of changes were made in this particular edit.</li>
	<li>Clearly and simply explain the context or boundaries of your study in the Description.The Description will act as the guide for future edits to this study. The description can be modified as any future developments of the study make it necessary.</li>
	<li>A great markdown 'cheat sheet' can be found at <a href="http://support.mashery.com/docs/customizing_your_portal/Markdown_Cheat_Sheet">http://support.mashery.com/docs/customizing_your_portal/Markdown_Cheat_Sheet</a></li>
	<li>You can autofill the fields in the form from your uploaded .md file using the following format in your .md file:
		<ul>
			<li><strong><u>=@title:</u></strong><br>Peace of God</li>
			<li><strong><u>=@comment:</u></strong><br>This is the first edit comment/li>
			<li><strong><u>=@description:</u></strong><br>I'm describing my study</li>
			<li><strong><u>=@body:</u></strong><br>#Here is the Title<br>
			## Subpoint 1<br>
			Here is a sentence<br>
			## Subpoint 2<br>...
			</li>
		</ul>
	</li>
</ul>